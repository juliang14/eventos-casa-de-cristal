			INNER JOIN	CARGO			AS E		ON A.CARGOID_CARGO 				= E.ID_CARGO
			WHERE C.ESTADO ='ACTIVO'
            AND C.NOMBRE_USUARIO = P_USUARIO
            AND C.CLAVE = P_CLAVE
